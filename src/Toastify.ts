import { toast } from "react-toastify";
import 'react-toastify/dist/ReactToastify.css';

export const notifyLoading = (msg:string)=> {
    return toast.loading( msg, {
        position: "top-center",
        autoClose: 1000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });
}

